    private void ShowData(String sql){
        DefaultTableModel data = new DefaultTableModel();
        data.addColumn ("No");
        data.addColumn ("No Transaksi");
        data.addColumn ("Kode Supplier");
        data.addColumn ("Nama Perusahaan");
        data.addColumn ("Tgl Transaksi");
        data.addColumn ("Nama Barang");
        data.addColumn ("Jumlah");
        data.addColumn ("Harga");
        data.addColumn ("Total");
        try{    
            int i = 1;
            st=con.createStatement();
            RSpembelian=st.executeQuery("select * from pembelian");
            while (RSpembelian.next())
                data.addRow(new Object[]{
                (""+i++),
                    RSpembelian.getString(1), RSpembelian.getString(2),
                    RSpembelian.getString(3), RSpembelian.getString(4),
                    RSpembelian.getString(5), RSpembelian.getString(6),
                    RSpembelian.getString(7), RSpembelian.getString(8)
                });
            tabelpembelian.setModel(data);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "ERROR \n Gagal Memuat KeDatabase \n Aktifkan Database Sebelum Memulai");
        }
    }
    
