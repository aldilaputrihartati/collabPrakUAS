    private void no_tranKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_no_tranKeyPressed
        // TODO add your handling code here:
        tran=no_tran.getText();
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try {
                sql="SELECT * FROM pembelian "
                        +"where kd_pembelian='"+ tran +"'";
                st=con.createStatement();
                RSpembelian=st.executeQuery(sql);
                while (RSpembelian.next()){
                    kd_sup.setText(RSpembelian.getString("kd_suplier"));
                    nm_p.setText(RSpembelian.getString(3));
                    tgl_tran.setDate(RSpembelian.getDate(4));
                    nm_brg.setText(RSpembelian.getString(5));
                    harga.setText(RSpembelian.getString(6));
                    jmlh.setText(RSpembelian.getString(7));
                    total.setText(RSpembelian.getString(8));
                    JOptionPane.showMessageDialog(null, "Data Ditemukan");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "ERROR \n Data Tidak Ada / Tidak Ditemukan" +e.getMessage());
                //nm_brg.requestFocus();
            }
        }
        
                    
        
    }//GEN-LAST:event_no_tranKeyPressed

